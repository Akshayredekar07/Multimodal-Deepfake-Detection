for epoch in range(num_epochs):
    model.train()
    running_loss = 0.0
    correct, total = 0, 0

    # Training phase
    progress_bar = tqdm(train_loader, desc=f"Epoch {epoch+1}/{num_epochs} [Training]")
    for waveforms, labels in progress_bar:
        waveforms, labels = waveforms.to(device), labels.to(device).float()  # Ensure labels are float for BCEWithLogitsLoss

        optimizer.zero_grad()
        outputs = model(waveforms).squeeze(1)  # Ensure correct shape
        loss = criterion(outputs, labels)
        loss.backward()

        torch.nn.utils.clip_grad_norm_(model.parameters(), max_norm=1.0)  # Prevent exploding gradients
        optimizer.step()

        running_loss += loss.item()
        preds = (torch.sigmoid(outputs) > 0.5).float()  # Apply sigmoid before thresholding
        correct += (preds == labels).sum().item()
        total += labels.size(0)

        progress_bar.set_postfix(loss=loss.item(), acc=correct / total * 100)

    epoch_train_loss = running_loss / len(train_loader)
    epoch_train_acc = correct / total * 100
    train_losses.append(epoch_train_loss)
    train_accuracies.append(epoch_train_acc)

    # Validation phase
    model.eval()
    val_loss, val_correct, val_total = 0.0, 0, 0

    with torch.no_grad():
        progress_bar = tqdm(val_loader, desc=f"Epoch {epoch+1}/{num_epochs} [Validation]")
        for waveforms, labels in progress_bar:
            waveforms, labels = waveforms.to(device), labels.to(device).float()

            outputs = model(waveforms).squeeze(1)
            loss = criterion(outputs, labels)

            val_loss += loss.item()
            preds = (torch.sigmoid(outputs) > 0.5).float()
            val_correct += (preds == labels).sum().item()
            val_total += labels.size(0)

            progress_bar.set_postfix(loss=loss.item(), acc=val_correct / val_total * 100)

    epoch_val_loss = val_loss / len(val_loader)
    epoch_val_acc = val_correct / val_total * 100
    val_losses.append(epoch_val_loss)
    val_accuracies.append(epoch_val_acc)


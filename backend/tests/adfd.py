def extract_features(audio_path, sr=16000, n_mfcc=40, min_duration=15):
    if not os.path.exists(audio_path):
        raise FileNotFoundError(f"File not found: {audio_path}")

    y, sr = librosa.load(audio_path, sr=sr)  # Load audio
    required_length = sr * min_duration  # 15 seconds in samples

    # Ensure audio is at least 15 sec long
    if len(y) < required_length:
        y = np.pad(y, (0, required_length - len(y)), mode='constant')
    else:
        y = y[:required_length]  # Trim excess

    # Extract MFCC (shape: (n_mfcc, time_steps))
    mfcc = librosa.feature.mfcc(y=y, sr=sr, n_mfcc=n_mfcc)
    mfcc = np.transpose(mfcc)  # Shape → (time_steps, n_mfcc)

    # Convert to tensor and return
    return torch.tensor(mfcc, dtype=torch.float32).unsqueeze(0).to(device)


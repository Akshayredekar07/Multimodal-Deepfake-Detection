class AudioFeatureDataset(Dataset):
    def __init__(self, feature_dir, subset):
        """
        Args:
            feature_dir (str): Path to the directory containing preprocessed features.
            subset (str): One of 'train', 'validation', or 'test'.
        """
        self.feature_dir = os.path.join(feature_dir, subset)
        self.real_features = [os.path.join(self.feature_dir, "real", f) for f in os.listdir(os.path.join(self.feature_dir, "real")) if f.endswith(".npy")]
        self.fake_features = [os.path.join(self.feature_dir, "fake", f) for f in os.listdir(os.path.join(self.feature_dir, "fake")) if f.endswith(".npy")]
        self.file_paths = self.real_features + self.fake_features
        self.labels = [0] * len(self.real_features) + [1] * len(self.fake_features)

    def __len__(self):
        return len(self.file_paths)

    def __getitem__(self, idx):
        feature = np.load(self.file_paths[idx])
        label = self.labels[idx]
        return torch.tensor(feature, dtype=torch.float32), torch.tensor(label, dtype=torch.float32)

